package com.sg.kata.bean;

public class Account {
	
	String referenceAccount ;
	double amount ;
	String foreignKeyClient ;
	
	
	
	public String getReferenceAccount() {
		return referenceAccount;
	}

	public void setReferenceAccount(String referenceAccount) {
		this.referenceAccount = referenceAccount;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

	public String getForeignKeyClient() {
		return foreignKeyClient;
	}

	public void setForeignKeyClient(String foreignKeyClient) {
		this.foreignKeyClient = foreignKeyClient;
	}
	
	
	
	
	
}
